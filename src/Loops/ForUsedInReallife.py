#for loop
for test_id in range(1,6):
    print(f"Running the test case:{test_id}")

#while loop
test_id=0
while test_id < 10:
    print("Running the testcase",test_id)
    test_id = test_id + 1

#print hi less than 3
count = 0
while count < 3:
    print("Hi")
    count = count + 1


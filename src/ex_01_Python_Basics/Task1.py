# Program to calculate the area of a triangle
# Formula: Area = 1/2 * base * height

# Given values
base = 10
height = 5

# Calculation
area = 0.5 * base * height

# Output (formatted)
print(f"The area of the triangle with base {base} and height {height} is {area:.2f}")


